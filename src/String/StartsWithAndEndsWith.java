package String;

public class StartsWithAndEndsWith {
    public static void main(String[] args) {
        String s1="Prakash";
        //To check the firts two Char is check
        System.out.println("StartsWith  String T/F:-"+s1.startsWith("Pr"));
        //To check the Last two Char is check
        System.out.println("EndsWith  String T/F:-"+s1.endsWith("sd"));

    }
}
