//java includes six relational operators:
//        == (equal to)
//        != (not equal to)
//        < (less than)
//        > (greater than)
//        <= (less than or equal to)
//        >= (greater than or equal to)
package Operators.Relational;

public class relational {
    public static void main(String[] args) {
        int a=10;
        int b=4;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);


    }
}
