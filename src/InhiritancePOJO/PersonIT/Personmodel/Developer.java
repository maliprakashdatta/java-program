package InhiritancePOJO.PersonIT.Personmodel;

public class Developer {
    String DeveloperName;
    String DeveloperType;
    int DeveloperSalary;
    public void setDeveloperName(String DeveloperName)
    {
        this.DeveloperName = DeveloperName;
    }

    public String getDeveloperName()
    {
        return DeveloperName;
    }
    public void setDeveloperType(String DeveloperType)
    {
        this.DeveloperType = DeveloperType;
    }

    public String getDeveloperType()
    {
        return DeveloperType;
    }
    public void setDeveloperSalary(int DeveloperSalary)
    {
        this.DeveloperSalary= DeveloperSalary;
    }

    public int getDeveloperSalary()
    {
        return DeveloperSalary;
    }

}
